    /**
     * Gets location-specific ASCII art.
     *
     * @param location The location to get art for
     * @return ASCII art for the specified location type
     */
    public static String getArtForLocation(Location location, GameState gameState) {
        if (gameState instanceof CombatState) {
            return COMBAT;
        }

        switch (location) {
            case Town town -> {
                return TOWN;
            }
            case Dungeon dungeon -> {
                return DUNGEON_1;
            }
            case Shop shop -> {
                if (shop.getShopType() == Shop.ShopType.BLACKSMITH) {
                    return SHOP_BLACKSMITH;
                } else if (shop.getShopType() == Shop.ShopType.TAVERN) {
                    return SHOP_TAVERN;
                } else {
                    return SHOP_GENERAL;
                }
            }
            case null, default -> {
                return getRandomWildernessArt();
            }
        }
    }
